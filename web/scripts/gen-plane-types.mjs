#!/usr/bin/env node
/**
 * gen-plane-types.mjs — generate the TypeScript types for the PUBLIC vault routes the app reads.
 *
 *   ../contracts/openapi/openapi.json  →  app/lib/plane/contract/schema.d.ts
 *
 * The vault's committed OpenAPI lives in the SAME repo (the Rust plane owns it), so there is no
 * lock file and no rev pinning here — the contract is read straight off disk and pruned to the
 * public routes the web tier calls (today: `GET /v1/enroll/verify/{user_code}`, the verification-
 * page disclosure). components.schemas ride whole so response schemas $ref freely.
 *
 * Usage:
 *   node scripts/gen-plane-types.mjs           regenerate the committed types
 *   node scripts/gen-plane-types.mjs --check    regenerate to memory + byte-compare (drift gate)
 */
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const WEB_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const OPENAPI_PATH = resolve(WEB_ROOT, "..", "contracts", "openapi", "openapi.json");
const TYPES_OUT = join(WEB_ROOT, "app", "lib", "plane", "contract", "schema.d.ts");

/** The PUBLIC routes the web tier calls with no credential — the only surface it needs types for. */
const PUBLIC_ROUTES = [{ method: "get", path: "/v1/enroll/verify/{user_code}" }];

const GENERATED_HEADER = `/**
 * Generated by scripts/gen-plane-types.mjs from ../contracts/openapi/openapi.json — do not edit.
 * Regenerate with: bun run gen:plane
 */
`;

/** Prune the vault contract to the public routes the app reads; keep components whole. */
function buildPublicDocument(document) {
  const paths = {};
  for (const { method, path } of PUBLIC_ROUTES) {
    const operation = document.paths?.[path]?.[method];
    if (!operation) {
      throw new Error(
        `public route missing from the vault contract: ${method.toUpperCase()} ${path}`,
      );
    }
    paths[path] = { [method]: operation };
  }
  return {
    openapi: document.openapi,
    info: {
      title: "topos plane — public read surface",
      description:
        "The public vault routes the web tier reads with no credential. Generated by " +
        "scripts/gen-plane-types.mjs — do not edit.",
      version: document.info?.version ?? "0.0.0",
    },
    paths,
    components: document.components,
  };
}

async function render() {
  const { default: openapiTS, astToString } = await import("openapi-typescript");
  const document = JSON.parse(readFileSync(OPENAPI_PATH, "utf8"));
  const ast = await openapiTS(buildPublicDocument(document));
  return GENERATED_HEADER + astToString(ast);
}

const check = process.argv.includes("--check");
const types = await render();

if (check) {
  let current;
  try {
    current = readFileSync(TYPES_OUT, "utf8");
  } catch {
    console.error(`contract drift: ${relOut()} is missing — run \`bun run gen:plane\``);
    process.exit(1);
  }
  if (current !== types) {
    console.error(`contract drift: ${relOut()} is stale — run \`bun run gen:plane\``);
    process.exit(1);
  }
  console.warn(`contract types in sync (${relOut()})`);
} else {
  mkdirSync(dirname(TYPES_OUT), { recursive: true });
  writeFileSync(TYPES_OUT, types);
  console.warn(`wrote ${relOut()}`);
}

function relOut() {
  return TYPES_OUT.slice(WEB_ROOT.length + 1);
}
